/**
 * Canonical curriculum — שאלון 581 first (algebra is the foundation),
 * then 582. Within each paper, ordered by pedagogical learning sequence
 * (foundation topics first, applications last).
 */
export const MATH5_CURRICULUM: TopicMapping[] = [...PAPER_581, ...PAPER_582];

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

/** Lookup the curriculum entry for a given topic. */
export function getTopicMapping(topic: string): TopicMapping | null {
  return MATH5_CURRICULUM.find((t) => t.key === topic) ?? null;
}

/** All topics belonging to a paper, in canonical order. */
export function topicsByPaper(paper: BagrutPaper): TopicMapping[] {
  return MATH5_CURRICULUM.filter((t) => t.paper === paper);
}

/** Returns the canonical index (0-based) of a topic in the curriculum.
 *  Useful for sorting any user-defined list back into bagrut order. */
export function curriculumIndex(topic: string): number {
  const i = MATH5_CURRICULUM.findIndex((t) => t.key === topic);
  return i === -1 ? Infinity : i;
}

/** Short label like "שאלון 581" — used by the BagrutBadge component. */
export function paperLabel(paper: BagrutPaper): string {
  return `שאלון ${paper}`;
}
