/**
 * bagrut-curriculum.ts — the canonical bagrut curriculum mapping.
 *
 * Every place in the app that lists math5 topics (picker, study plan,
 * onboarding, lesson page) should consume this module instead of
 * hard-coding its own order or labels. The data here reflects the
 * official Ministry of Education syllabus for math5 (שאלון 581 + 582)
 * and the empirical breakdown of point weights based on bagrut exams
 * from 2020-2025.
 *
 * Why this exists:
 *  - The student opens the app and wants to feel "this is exactly my
 *    bagrut, in the right order".
 *  - Every lesson page needs to say "this is from שאלון 581, worth
 *    20-25 points, appears in every bagrut".
 *  - The picker should group by paper with section headers.
 *  - The default plan generated by onboarding should be in the right
 *    pedagogical order (שאלון 581 first because it's the foundation).
 */

export type BagrutPaper = '581' | '582';

/** How "core" the topic is — affects visual emphasis in the UI. */
export type TopicWeight = 'core' | 'standard' | 'optional';

/** Frequency of appearance in actual bagruyot — short Hebrew phrase. */
export type AppearsIn = 'בכל בגרות' | 'ברוב הבגרויות' | 'בכמחצית מהבגרויות' | 'אופציונלי';

export type TopicMapping = {
  /** Topic key — must match the topic name registered in lessons/index.ts. */
  key: string;
  /** Hebrew display name (usually identical to `key`). */
  displayName: string;
  /** Short emoji for the topic — matches existing /quiz pickers. */
  emoji: string;
  /** Primary bagrut paper. */
  paper: BagrutPaper;
  /** Whether the topic also appears in the other paper (rare). */
  alsoIn?: BagrutPaper;
  /** Visual emphasis. */
  weight: TopicWeight;
  /** How often it appears in real bagruyot. */
  appearsIn: AppearsIn;
  /** Typical point value when it does appear (a question is 20-25 pts). */
  typicalPoints: string;
  /** One-line description of how the topic is examined. */
  examStyle: string;
};

